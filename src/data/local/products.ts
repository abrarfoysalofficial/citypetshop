export const featuredProductIds = ["1", "5", "9", "11", "17", "18"];

export async function getProducts(): Promise<Product[]> {
  return products;
}

export async function getFeaturedProducts(): Promise<Product[]> {
  return products.filter((p) => featuredProductIds.includes(p.id));
}

/** Flash sale: products with tag "Flash Sale" or "flash_sale". Admin can drive via tags. */
export async function getFlashSaleProducts(limit = 8): Promise<Product[]> {
  const tagSet = new Set(["Flash Sale", "flash_sale", "Flash sale"]);
  return products
    .filter((p) => p.tags?.some((t) => tagSet.has(t)))
    .slice(0, limit);
}

/** Clearance: products with comparePrice (on sale). Admin can drive via compare price. */
export async function getClearanceProducts(limit = 8): Promise<Product[]> {
  return products
    .filter((p) => p.comparePrice != null && p.comparePrice > p.price)
    .slice(0, limit);
}

/** Rule-based recommended: same category, exclude one id; AI-ready to extend. */
export async function getRecommendedProducts(
  categorySlug: string,
  excludeId: string,
  limit = 4
): Promise<Product[]> {
  return products
    .filter((p) => p.categorySlug === categorySlug && p.id !== excludeId)
    .slice(0, limit);
}
export async function getProductById(id: string): Promise<Product | null> {
  return products.find((p) => p.id === id) ?? null;
}

export async function getProductBySlug(slug: string): Promise<Product | null> {
  return products.find((p) => p.slug === slug) ?? null;
}
